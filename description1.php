      <h3><b>ABOUT CONVENTIONAL CENTER</b></h3>
      <p>The Kongu Convention Centre located at Erode in Tamil Nadu, India consists of a multipurpose hall which is one of the largest in India with a seating capacity of 4500 persons. Alternatively it has one indoor basket ball court and four indoor badminton courts which are used as sports halls. The building also has the facilities for Conference halls , Seminar halls , Training centre , Guest rooms and VIP rooms , Offices , High-tech Audio Visual facilities for Global conferencing , Cafeteria , Offices etc. The construction completed in July 2010 with a built up area of 18,600 sq metres.
The Architects Prof. Charanjit Shah and Ar. Gurpreet Shah wanted to break the monotony of the Kongu Engineering Campus. A form that is dynamic and can define identity of the campus was contemplated. The main auditorium of the building is flanked by 2 additional seating aisles on either side which augment the capacity of the auditorium when required. When not in use as an auditorium it functions as an indoor sports complex for various sports activities throughout the year.
        
        
       </p>
